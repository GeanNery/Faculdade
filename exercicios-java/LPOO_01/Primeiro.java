// Exemplos de definição de variáveis

public class Primeiro
{
	public static void main(String[] args)
	{
		char caractere = '$';
		String texto = "Snoop Dogg";
		int inteiro = 345;
		double decimal = 678.35;
		boolean condicao = false;
		
		System.out.print(
		"Variável tipo char: " + caractere 	  +
		"\nVariável tipo String: " + texto    +
		"\nVariável tipo int: " + inteiro 	  +
		"\nVariável tipo double: " + decimal  +
		"\nVariável tipo boolean: " + condicao);
	}
}
