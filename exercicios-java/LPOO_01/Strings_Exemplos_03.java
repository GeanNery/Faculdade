public class Strings_Exemplos_03
{
	public static void main(String[] args)
	{
		String textoExemplo = "A LinGuAgeM JaVA";
		
		System.out.printf("%s%n",textoExemplo);
		System.out.printf("%n%s%n", textoExemplo.toLowerCase()); // será impresso "a linguagem java"
		System.out.printf("%n%s", textoExemplo.toUpperCase()); 	 // será impresso "A LINGUAGEM JAVA"
	}
}
